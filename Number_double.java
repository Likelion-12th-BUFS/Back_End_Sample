class Number_double {
    public static void main(String[] args) {
        double a = 23.5;
        System.out.println(a);

        double b = 20;
        System.out.println(b);

        System.out.println(a + b);

        a = 0.1234567890123456789;
        System.out.println(a); // float에서는 0.123456789까지만 출력
    }
}
